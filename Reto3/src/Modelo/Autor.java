package Modelo;

public class Autor {
	
	private String ID;
	private String Url;
	private String Eid;
	private String document_count;
	private String affiliationName;
	private String affiliationCity;
	private String affiliationContry;
	
	public Autor() {
		
	}
	
	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getUrl() {
		return Url;
	}
	public void setUrl(String Url) {
		this.Url = Url;
	}
	public String getEid() {
		return Eid;
	}
	public void setEid(String Eid) {
		this.Eid = Eid;
	}
	public String getdocument_count() {
		return document_count;
	}
	public void setdocument_count(String document_count) {
		this.document_count = document_count;
	}
	
	public String getaffiliationName() {
		return affiliationName;
	}
	public void setaffiliationName(String affiliationName) {
		this.affiliationName = affiliationName;
	}
	public String getaffiliationCity() {
		return affiliationCity;
	}
	public void setaffiliationCity(String affiliationCity) {
		this.affiliationCity = affiliationCity;
	}
	public String getaffiliationContry() {
		return affiliationContry;
	}
	public void setaffiliationContry(String affiliationContry) {
		this.affiliationContry = affiliationContry;
	}
	
	
}	
